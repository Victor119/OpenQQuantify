def generate_am_signal(sample_rate, modulation_index, modulation_frq, carrier_freq, duration, output_dir):
    qapp = Qt.QApplication(sys.argv)
    
    tb = low_frequency(
        samp_rate=sample_rate,
        mi = modulation_index,
        fm = modulation_frq,
        fc = carrier_freq
    )
    
    # Create a file_sink block
    output_dir_path = os.path.join(output_dir, f"am_signal_{carrier_freq}_{modulation_index}_{modulation_frq}_{sample_rate}.dat")
    file_sink = blocks.file_sink(gr.sizeof_float, output_dir_path)
    file_sink.set_unbuffered(True)

    # Connect the output of the modulation block to the file sink
    tb.connect((tb.blocks_throttle2_0, 0), (file_sink, 0))
    
    tb.start()
    tb.show()

    Qt.QTimer.singleShot(duration, lambda: qapp.quit())

    def sig_handler(sig=None, frame=None):
        tb.stop()
        tb.wait()
        Qt.QApplication.quit()

    signal.signal(signal.SIGINT, sig_handler)
    signal.signal(signal.SIGTERM, sig_handler)
    
    qapp.exec_()
    
    tb.stop()
    tb.wait()


